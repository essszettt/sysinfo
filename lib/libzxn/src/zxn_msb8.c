/*----------------------------------------------------------------------------*/
/* zxn_msb8()                                                                 */
/*----------------------------------------------------------------------------*/
int8_t zxn_msb8(uint8_t uiValue)
{
  int iReturn = 0;

  if (0 != uiValue)
  {
    while (uiValue >>= 1)
    {
      ++iReturn;
    }
  }
  else
  {
    iReturn = -1 * EINVAL; /* error: no bit set */
  }

  return iReturn;
}


