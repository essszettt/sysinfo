/*----------------------------------------------------------------------------*/
/* zxn_radastan_mode()                                                        */
/*----------------------------------------------------------------------------*/
bool zxn_radastan_mode(void)
{
  return (0 != ((ZXN_READ_REG(REG_L10_CONTROL) >> 5) & 0x01));
}


