/*----------------------------------------------------------------------------*/
/* zxn_rtrim()                                                                */
/*----------------------------------------------------------------------------*/
char_t* zxn_rtrim(char_t* acString)
{
  if (acString)
  {
    char_t* pEnd = acString + strlen(acString);

    while (pEnd > acString)
    {
      --pEnd;

      if (' ' >= *pEnd)
      {
        *pEnd = '\0';
      }
      else
      {
        break;
      }
    }

    return acString;
  }

  return 0;
}


